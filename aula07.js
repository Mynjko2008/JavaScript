// Operadores básicos em JavaScript:
// +  = Adição
// -  = Subtração
// /  = Divisão
// *  = Multiplicação
// %  = Resto da divisão (módulo)

const resultadoAdicao = 7 + 45;
console.log(resultadoAdicao);

const resultadoSubtracao = 50 - 80;
console.log(resultadoSubtracao);

const resultadoDivisao  = 40 / 60;
console.log(resultadoDivisao);

const resultadoMultiplicacao = 20 * 79;
console.log(resultadoMultiplicacao);

// Operador de concatenação (+) também é usado para unir strings
const texto1 = "Olá, meu nome é Jonas";
const texto2 = "Eu tenho 23 anos";

const textoFinal = texto1 + " " + texto2;
console.log(textoFinal);

// Exemplo do operador módulo (%), que retorna o resto da divisão
let x = 5;
let y = 2;
let z = x % y;
console.log(z);  // Saída: 1

